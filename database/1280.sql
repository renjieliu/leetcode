select distinct sj.student_id, sj.student_name, sj.subject_name, attended_exams = isnull(cnt, 0) from (select * from  Students  , Subjects) sj left outer join 
(
	select student_id,subject_name , cnt = count(subject_name)	 from Examinations
	group by student_id, subject_name
) e
on sj.student_id = e.student_id and sj.subject_name  = e.subject_name 
